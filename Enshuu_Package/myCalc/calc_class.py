class Calculate():
    def __init__(self,a,b):
        self.a=a
        self.b=b
        print("a={} b={}".format(a,b))
    def sum(self):
        return self.a+self.b
    def dif(self):
        return self.a-self.b
    def multi(self):
        return self.a*self.b
    def div(self):
        return self.a/self.b
    
    

    